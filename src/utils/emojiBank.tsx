export const emojifySentence = (text: string): string => {
  const amountEmoji = getRandomInt(0, 1)
  const trailingChance = 0.3
  const textSplit = text.split(' ')
  for (let i = 0; i <= amountEmoji; i++) {
    textSplit.splice(
      Math.floor(Math.random() * textSplit.length),
      0,
      emojiBank[Math.floor(Math.random() * emojiBank.length)]
    )
  }
  if (Math.random() * 1 < trailingChance)
    textSplit.push(emojiBank[Math.floor(Math.random() * emojiBank.length)])
  return textSplit.join(' ')
}

export const emojifyBetweenWords = (text: string): string => {
  text.replace(/\n/g, 'NEWLINE')
  const textSplit = text.split(' ')
  const amountEmoji = Math.floor(Math.random() * (text.split.length * 0.6)) + 2
  for (let i = 0; i <= amountEmoji; i++) {
    textSplit.splice(
      Math.floor(Math.random() * (textSplit.length - textSplit.length * 0.2)) +
        textSplit.length * 0.1,
      0,
      emojiBank[Math.floor(Math.random() * emojiBank.length)]
    )
  }
  const newText = textSplit.join(' ').replace('NEWLINE', '\n\n')
  return newText
}
